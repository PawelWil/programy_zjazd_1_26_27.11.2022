# KWADRAT o DŁUGOŚCI BOKU 2 cm
print ("\tkwadrat o długości boku 2")
a = 2.
 #Pole powierzchni kwadratu, dla boku o długości 2cm
p = a ** 2
print ("Pole powierzchni wynosi:", p )
 #Obwód kwadratu, dla boku o długości 2cm
obwod = 4 * a
print ("Obwód kwadratu wynosi:", obwod)
 # Długość przekątnej kwadratu, dla boku o długości 2cm
dlugosc_przekatnej = a * 2 ** .5
print ("Długość przekątnej kwadratu wynosi:", dlugosc_przekatnej)


